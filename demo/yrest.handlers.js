/**
 * Handler for POST /api/auth/login
 * Validates credentials and returns a session token.
 * Credentials: ana@demo.com / demo123, luis@demo.com / demo123
 * @param {import('@aggiovato/yrest').HandlerRequest} req
 * @returns {Promise<import('@aggiovato/yrest').HandlerResponse>}
 */
export async function login(req) {
  const { email, password } = req.body ?? {};

  if (!email || !password) {
    return { status: 400, body: { error: "Email and password are required" } };
  }

  const USERS = {
    "ana@demo.com": {
      userId: 1,
      name: "Ana García",
      avatar: "AG",
      role: "admin",
      password: "demo123",
    },
    "luis@demo.com": {
      userId: 2,
      name: "Luis Martín",
      avatar: "LM",
      role: "user",
      password: "demo123",
    },
  };

  const user = USERS[email];
  if (!user || user.password !== password) {
    return { status: 401, body: { error: "Invalid credentials" } };
  }

  return {
    status: 200,
    body: {
      token: `yrest-${Date.now()}`,
      expiresIn: 3600,
      user: { email, userId: user.userId, name: user.name, avatar: user.avatar, role: user.role },
    },
  };
}

/**
 * Handler for GET /api/stats
 * Returns live server statistics — values change on every request.
 * @returns {Promise<import('@aggiovato/yrest').HandlerResponse>}
 * @param _req
 */
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export async function stats(_req) {
  return {
    status: 200,
    body: {
      generatedAt: new Date().toISOString(),
      // eslint-disable-next-line no-undef
      requestId: crypto.randomUUID(),
      server: "yrest-demo",
      // eslint-disable-next-line no-undef
      uptime: process.uptime().toFixed(2) + "s",
      note: "This response is generated by a handler function on every request",
    },
  };
}

/**
 * Handler for GET /api/summary
 * Aggregates all todos and users to produce per-user stats and a
 * tasks-per-day chart payload. Data is fetched from the yrest API itself.
 * @returns {Promise<import('@aggiovato/yrest').HandlerResponse>}
 */
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export async function summary(_req) {
  const base = "http://localhost:3070/api";

  const [todosRes, usersRes] = await Promise.all([
    // eslint-disable-next-line no-undef
    fetch(`${base}/todos?_limit=1000`).then((r) => r.json()),
    // eslint-disable-next-line no-undef
    fetch(`${base}/users`).then((r) => r.json()),
  ]);

  const todos = Array.isArray(todosRes) ? todosRes : [];
  const users = Array.isArray(usersRes) ? usersRes : [];

  const userStats = users.map((user) => {
    const mine = todos.filter((t) => t.userId === user.id);
    return {
      id: user.id,
      name: user.name,
      avatar: user.avatar,
      total: mine.length,
      done: mine.filter((t) => t.done).length,
      pending: mine.filter((t) => !t.done).length,
    };
  });

  // Group by createdAt date → { [date]: { [userId]: count } }
  const dateMap = {};
  for (const todo of todos) {
    const d = todo.createdAt ?? "unknown";
    if (!dateMap[d]) dateMap[d] = {};
    dateMap[d][todo.userId] = (dateMap[d][todo.userId] ?? 0) + 1;
  }

  const byDate = Object.entries(dateMap)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([date, counts]) => ({ date, counts }));

  return {
    status: 200,
    body: {
      generatedAt: new Date().toISOString(),
      totalTodos: todos.length,
      totalUsers: users.length,
      userStats,
      byDate,
    },
  };
}
