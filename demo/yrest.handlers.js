// yrest handlers — loaded via "handlers: ./yrest.handlers.js" in yrest.config.yml
// Handler signature: (req: HandlerRequest) => HandlerResponse | Promise<HandlerResponse>

/**
 * Handler for POST /api/auth/login
 * Validates credentials and returns a session token.
 * Credentials: ana@demo.com / demo123, luis@demo.com / demo123
 * @param {import('@aggiovato/yrest').HandlerRequest} req
 * @returns {Promise<import('@aggiovato/yrest').HandlerResponse>}
 */
export async function login(req) {
  const { email, password } = req.body ?? {};

  if (!email || !password) {
    return { status: 400, body: { error: "Email and password are required" } };
  }

  const CREDENTIALS = {
    "ana@demo.com": "demo123",
    "luis@demo.com": "demo123",
  };

  if (!CREDENTIALS[email] || CREDENTIALS[email] !== password) {
    return { status: 401, body: { error: "Invalid credentials" } };
  }

  return {
    status: 200,
    body: {
      token: `yrest-${Date.now()}`,
      expiresIn: 3600,
      user: { email, name: email.split("@")[0] },
    },
  };
}

/**
 * Handler for GET /api/stats
 * Returns live server statistics — values change on every request.
 * @param {import('@aggiovato/yrest').HandlerRequest} req
 * @returns {Promise<import('@aggiovato/yrest').HandlerResponse>}
 */
export async function stats(req) {
  return {
    status: 200,
    body: {
      generatedAt: new Date().toISOString(),
      requestId: crypto.randomUUID(),
      server: "yrest-demo",
      uptime: process.uptime().toFixed(2) + "s",
      note: "This response is generated by a handler function on every request",
    },
  };
}
